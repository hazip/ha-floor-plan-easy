// Import/export of a floor as a human-readable .json file.
//
// The exported file carries a leading /* ... */ comment block (name, id,
// version, timestamp). JSON has no native comments, so that block is stripped
// again before JSON.parse() on import. These functions are pure (no DOM): the
// toolbar handles the actual download/file-picker.

export function serializeFloorExport(floorJson, { exportedAt } = {}) {
  const pretty = JSON.stringify(floorJson, null, 2);

  // Neutralize any "*/" in a value so it cannot close the comment block early
  // (which would corrupt the header and break re-import).
  const safe = (v) => String(v ?? "").replace(/\*\//g, "* /");

  const header = [
    "/*",
    " * FloorPlan Easy — floor export",
    ` * Name:     ${safe(floorJson.name)}`,
    ` * Id:       ${safe(floorJson.id)}`,
    ` * Version:  ${safe(floorJson.version ?? 1)}`,
    ` * Exported: ${safe(exportedAt)}`,
    " *",
    " * Generated by the FloorPlan Easy editor. You may edit this file by hand.",
    " * The comment block above is ignored when the file is re-imported.",
    " */",
    "",
  ].join("\n");

  return header + pretty + "\n";
}

export function parseFloorExport(text) {
  // Strip an optional BOM and a single leading block comment so the remainder
  // is plain JSON. The header we write contains no "*/", so the non-greedy
  // match ends exactly at the end of the comment block.
  const json = String(text)
    .replace(/^﻿/, "")
    .replace(/^\s*\/\*[\s\S]*?\*\//, "")
    .trim();

  const data = JSON.parse(json);

  if (!data || typeof data !== "object" || !data.id) {
    throw new Error("Not a valid FloorPlan Easy export (missing id).");
  }

  return data;
}
